//add
function add(a,b){
    return a+b
}


//subtract
function subtract(a,b){
    return a-b
}


//multiply
function multiply(a,b){
    return a*b
}


//divide
function divide(a,b){
    return a/b
}


console.log(add(10,20))
console.log(subtract(10,20))
console.log(multiply(5,10))
console.log(divide(10,20))